export function add(num1: number, num2: number): number {
  let add: number = num1 + num2;
  return add;
}

export function substract(num1: number, num2: number): number {
  let subs: number = num1 - num2;
  return subs;
}

export function multi(num1: number, num2: number): number {
  let multi: number = num1 * num2;
  return multi;
}

export function divi(num1: number, num2: number): number {
  let divi: number = num1 / num2;
  return divi;
}
